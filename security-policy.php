                            <p class="custom-para text-justify mt-4">
                                Our robust security framework based on OWASP standards, implemented in the application
                                layer, provides functionalities to mitigate threats such as SQL injection, Cross-site
                                scripting and application layer DOS attacks.
                            </p>

                            <h4 class="font-weight-bold mt-5">Data isolation</h4>
                            <p class="custom-para text-justify mt-4">
                                Our framework distributes and maintains the cloud space for our customers. Each
                                customer's service data is logically separated from other customers' data using a set of
                                secure protocols in the framework. This ensures that no customer's service data becomes
                                accessible to another customer. Users don't have to worry about data isolation in the
                                case of an on-premise cloud.
                            </p>

                            <p class="custom-para text-justify mt-4">
                                The service data is stored on our servers when you use our services. Your data is owned
                                by you, and not by xStack. We do not share this data with any third-party without your
                                consent.
                            </p>

                            <h4 class="font-weight-bold mt-5">Encryption</h4>
                            <p class="custom-para text-justify mt-4">
                                <b>In transit:</b> All customer data transmitted to our servers over public networks is
                                protected using strong encryption protocols. We mandate all connections to our servers
                                use Transport Layer Security (TLS 1.2/1.3) encryption with strong ciphers, for all
                                connections including web access, API access, our mobile apps and IMAP/POP/SMTP email
                                client access. This ensures a secure connection by allowing the authentication of both
                                parties involved in the connection, and by encrypting data to be
                                transferred. Additionally for email, our services leverages opportunistic TLS by
                                default. TLS encrypts and delivers content securely, mitigating eavesdropping between
                                servers where peer services support this protocol.
                            </p>

                            <p class="custom-para text-justify mt-4">
                                We have full support for Perfect Forward Secrecy (PFS) with our encrypted connections,
                                which ensures that even if we were somehow compromised in the future, no previous
                                communication could be decrypted. We have enabled HTTP Strict Transport Security header
                                (HSTS) to all our web connections. This tells all modern browsers to only connect to us
                                over an encrypted connection, even if you type a URL to an insecure page at our
                                site. Additionally, on the web we flag all our authentication cookies as secure.
                            </p>

                            <p class="custom-para text-justify mt-4">
                                <b>At rest:</b> Customer data at rest is encrypted using 256-bit Secure Hashing
                                Algorithm
                                (SHA-2). We own and maintain the keys using our in-house Key Management Service (KMS).
                                We
                                provide additional layers of security by encrypting the data encryption keys using
                                master keys. The master keys and data encryption keys are physically separated and
                                stored in different servers with limited access.
                            </p>

                            <h4 class="font-weight-bold mt-5">Data retention and disposal</h4>
                            <p class="custom-para text-justify mt-4">
